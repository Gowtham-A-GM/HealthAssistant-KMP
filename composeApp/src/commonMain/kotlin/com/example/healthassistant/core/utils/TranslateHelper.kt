@Composable
fun t(text: String): String {
    val language = LanguageState.currentLanguage.value

    // Fast path: English (no translation needed)
    if (language == "en") return text

    // Fast path: static lookup (exact, instant, no ML Kit call)
    val static = staticTranslations[text]?.get(language)
    if (static != null) return static

    // Slow path: dynamic content — use ML Kit live translation
    var translated by remember(text, language) { mutableStateOf(text) }
    LaunchedEffect(text, language) {
        translated = platformTranslate(text)
    }
    return translated
}